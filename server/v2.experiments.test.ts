import assert from "node:assert/strict";
import { randomUUID } from "crypto";
import { boundedRandom, ExperimentsV2EventTypes, ExperimentsV2Service, parameterGrid } from "./v2/experiments";
const service = new ExperimentsV2Service();
const input = { hypothesisId: "hyp", strategyId: "strat", strategyVersion: 1, experimentType: "parameter_grid" as const, datasetSpecification: { symbols: ["EUR_USD"], timeframes: ["1h"], start: "2020-01-01", end: "2021-01-01" }, parameterSpecification: { grid: { atr: [1, 1.5], target: [1.5, 2] } }, holdoutPolicy: { trainEnd: "2020-06-01", validationEnd: "2021-01-01", testStart: "2022-01-01", finalHoldoutLocked: true as const }, randomSeed: "seed-1", resourceBudget: { maxCandles: 1000, maxRuntimeMs: 10000 }, priority: 10, maxAttempts: 2, correlationId: randomUUID(), causationId: randomUUID() };
assert.equal(parameterGrid(input.parameterSpecification.grid).length, 4);
assert.deepEqual(boundedRandom(input.parameterSpecification.grid, 2, "seed").length, 2);
const created = service.create(input);
assert.equal(created.experiment.status, "queued");
assert.ok(created.events.some(e => e.eventType === ExperimentsV2EventTypes.ExperimentQueued));
const dup = service.create(input);
assert.equal(dup.events[0].eventType, ExperimentsV2EventTypes.ExperimentDuplicateSuppressed);
const leased = service.lease("worker-1", new Date("2026-01-01T00:00:00.000Z"));
assert.equal(leased?.status, "leased");
assert.equal(service.lease("worker-2", new Date("2026-01-01T00:00:01.000Z")), null);
const recovered = service.lease("worker-2", new Date("2026-01-01T00:02:00.000Z"));
assert.equal(recovered?.leaseOwner, "worker-2");
assert.throws(() => service.create({ ...input, datasetSpecification: { ...input.datasetSpecification, end: "2023-01-01" } }), /holdout leakage/);
assert.equal(service.cancel(created.experiment.experimentId).status, "cancelled");
assert.equal("runBacktest" in service, false);
console.log("v2 phase 10 experiments tests passed");
